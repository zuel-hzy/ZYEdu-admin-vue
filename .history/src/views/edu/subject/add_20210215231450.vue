<template>
    <div class="app-container">       
        <el-form label-width="120px">
            <el-form-item label="信息描述">
                <el-tag type="info">excel模版说明</el-tag>
                <el-tag>
                    <i class="el-icon-download"/>
                    <a :href="OSS_PATH +'/excel/test.xlsx'">点击下载模版</a>
                </el-tag>
            </el-form-item>
            <el-form-item label="选择Excel">
                <el-upload
                    ref="upload"
                    :auto-upload="false"
                    :on-success="fileUploadSuccess"
                    :on-error="fileUploadError"
                    :disabled="importBtnDisabled"
                    :limit="1"
                    :action="BASE_API+'/edu_teacher/edu-subject/addSubject'"
                    name="file">
                <el-button 
                    slot="trigger" 
                    size="small" 
                    type="primary">选取文件
                </el-button>
                <el-button
                    :loading="loading"
                    style="margin-left: 10px;"
                    size="small"
                    type="success"
                    @click="submitUpload">
                    上传到服务器
                </el-button>
                </el-upload>
            </el-form-item>       
        </el-form>  
    </div>
</template>

<script>
export default {
    props: {

    },
    data() {
        return {
            BASE_API: process.env.VUE_APP_BASE_API, // 接口API地址
            OSS_PATH: process.env.VUE_APP_OSS_PATH, // 阿里云OSS地址
            importBtnDisabled: false, // 按钮是否禁用,
            loading: false
        }
    },
    computed: {

    },
    created() {

    },
    mounted() {

    },
    watch: {

    },
    methods: {
        fileUploadSuccess(){
            console.log(response)
            this.fileUploadBtnText = '导入失败'       
            this.loading = false       
            this.$message({       
                type: 'error',       
                message: '导入失败'    
            })
        },
        fileUploadError(response){
            if (response.success ===  true) {       
                this.fileUploadBtnText = '导入成功'       
                this.loading = false       
                this.$message({
                    type: 'success',
                    message: response.message    
                })    
            }
        },
        submitUpload(response){
            this.importBtnDisabled = true
            this.loading = true
            this.$refs.upload.submit()
        },
    },
    components: {

    },
};
</script>

